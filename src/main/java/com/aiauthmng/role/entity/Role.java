package com.aiauthmng.role.entity;

/**
* 角色实体
* 角色实体
* @author chaoyalong
* @date 2018-5-25 15:16
*/
public class Role {

    private int id;
    private String code;
    private String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
